type Dict = (typeof dict)["pt"];

interface LangState {
  lang: Lang;
  setLang: (l: Lang) => void;
}

export const useLangStore = create<LangState>()(
  persist((set) => ({ lang: "pt", setLang: (lang) => set({ lang }) }), {
    name: "quizfunnel-lang",
  }),
);

export function useT(): Dict {
  const lang = useLangStore((s) => s.lang);
  return dict[lang] as Dict;
}
